class Army:
    def __init__(self):
        self.army = []

    def add_units(self, soldier, nums):
        if _ in range(nums):
            self.army.append(soldier())


class Warrior:
    def __init__(self):
        self.health = 50
        self.attack = 5
        self.defense = 0

    @property
    def is_alive(self):
        return self.health > 0


class Knight(Warrior):
    def __init__(self):
        super().__init__()
        self.attack = 7


class Defender(Warrior):
    def __init__(self):
        super().__init__()
        self.health = 60
        self.attack = 3
        self.defense = 2


class Battle:
    def fight(self, army1, army2):
        while army1.army and army2.army:
            if fight(army1.army[0], army2.army[0]):
                army2.army.pop(0)
            else:
                army1.army.pop(0)
        return True if army1.army else False


def fight(unit_1, unit_2):
    damage1 = unit_1.attack - unit_2.defense
    if damage1 < 0:
        damage1 = 0
    damage2 = unit_2.attack - unit_1.defense
    if damage2 < 0:
        damage2 = 0
    while unit_1.is_alive and unit_2.is_alive:
        unit_2.health -= damage1
        if unit_2.is_alive:
            unit_1.health -= damage2
    return unit_1.is_alive


